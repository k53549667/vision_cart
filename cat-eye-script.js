// Generate stars
function generateStars(rating) {
    let stars = '';
    const fullStars = Math.floor(rating);
    const hasHalfStar = rating % 1 !== 0;
    
    for (let i = 0; i < fullStars; i++) {
        stars += '<i class="fas fa-star"></i>';
    }
    
    if (hasHalfStar) {
        stars += '<i class="fas fa-star-half-alt"></i>';
    }
    
    const emptyStars = 5 - Math.ceil(rating);
    for (let i = 0; i < emptyStars; i++) {
        stars += '<i class="far fa-star"></i>';
    }
    
    return stars;
}

// Setup filters
function setupCatEyeFilters() {
    const filterCheckboxes = document.querySelectorAll('.filter-options input[type="checkbox"]');
    
    filterCheckboxes.forEach(checkbox => {
        checkbox.addEventListener('change', applyCatEyeFilters);
    });
    
    const clearButton = document.querySelector('.clear-filters');
    if (clearButton) {
        clearButton.addEventListener('click', clearCatEyeFilters);
    }
}

// Apply filters
function applyCatEyeFilters() {
    const selectedFilters = {
        frameType: [],
        color: [],
        brand: [],
        price: [],
        gender: [],
        material: []
    };
    
    document.querySelectorAll('.filter-options input[type="checkbox"]:checked').forEach(checkbox => {
        const filterType = checkbox.name;
        const filterValue = checkbox.value;
        if (selectedFilters[filterType]) {
            selectedFilters[filterType].push(filterValue);
        }
    });
    
    let filteredProducts = catEyeProducts.filter(product => {
        if (selectedFilters.frameType.length > 0 && !selectedFilters.frameType.includes(product.frameType)) {
            return false;
        }
        
        if (selectedFilters.color.length > 0 && !selectedFilters.color.includes(product.color)) {
            return false;
        }
        
        if (selectedFilters.brand.length > 0) {
            const brandMatch = selectedFilters.brand.some(brand => 
                product.brand.toLowerCase().includes(brand.replace('-', ' '))
            );
            if (!brandMatch) return false;
        }
        
        if (selectedFilters.price.length > 0) {
            const priceMatch = selectedFilters.price.some(range => {
                if (range === '0-1000') return product.price < 1000;
                if (range === '1000-2000') return product.price >= 1000 && product.price < 2000;
                if (range === '2000-3000') return product.price >= 2000 && product.price < 3000;
                if (range === '3000+') return product.price >= 3000;
                return false;
            });
            if (!priceMatch) return false;
        }
        
        if (selectedFilters.gender.length > 0 && !selectedFilters.gender.includes(product.gender)) {
            return false;
        }
        
        if (selectedFilters.material.length > 0 && !selectedFilters.material.includes(product.material)) {
            return false;
        }
        
        return true;
    });
    
    loadCatEyeProducts(filteredProducts);
}

// Clear all filters
function clearCatEyeFilters() {
    document.querySelectorAll('.filter-options input[type="checkbox"]').forEach(checkbox => {
        checkbox.checked = false;
    });
    loadCatEyeProducts(catEyeProducts);
}

// Setup sort
function setupCatEyeSort() {
    const sortSelect = document.getElementById('sortSelect');
    if (sortSelect) {
        sortSelect.addEventListener('change', function() {
            sortCatEyeProducts(this.value);
        });
    }
}

// Sort products
function sortCatEyeProducts(sortBy) {
    let sortedProducts = [...catEyeProducts];
    
    switch(sortBy) {
        case 'price-low':
            sortedProducts.sort((a, b) => a.price - b.price);
            break;
        case 'price-high':
            sortedProducts.sort((a, b) => b.price - a.price);
            break;
        case 'rating':
            sortedProducts.sort((a, b) => b.rating - a.rating);
            break;
        case 'newest':
            sortedProducts.reverse();
            break;
        default:
            sortedProducts.sort((a, b) => b.reviews - a.reviews);
    }
    
    loadCatEyeProducts(sortedProducts);
}

// Setup search
function setupCatEyeSearch() {
    const searchInput = document.getElementById('searchInput');
    if (searchInput) {
        searchInput.addEventListener('input', function() {
            searchCatEyeProducts(this.value);
        });
    }
}

// Search products
function searchCatEyeProducts(searchTerm) {
    const filtered = catEyeProducts.filter(product => {
        const searchLower = searchTerm.toLowerCase();
        return product.name.toLowerCase().includes(searchLower) ||
               product.brand.toLowerCase().includes(searchLower) ||
               product.type.toLowerCase().includes(searchLower);
    });
    
    loadCatEyeProducts(filtered);
}

// Update product count
function updateCatEyeProductCount(count) {
    const productCountElement = document.getElementById('productCount');
    if (productCountElement) {
        productCountElement.textContent = count;
    }
}

